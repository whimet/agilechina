package com.google.code.calc;

//add comments

public class Calc {
    public Integer add(int digita, int digitb) {
        return new Integer(digita + digitb);
    }

    public Integer subtract(int digita, int digitb) {
        return new Integer(digita - digitb);
    }

    public Integer multiply(int digita, int digitb) {
        return new Integer(digita * digitb);
    }

    public Integer divide(int digita, int digitb) {
        return new Integer(digita / digitb);
    }
}
